const models = require("../models");

const browse = (req, res) => {
  let where = "";
  if (req.query.theme != null) {
    where = ` where themeID = '${req.query.theme}'`;
  }
  models.tutorial
    .findAllTutorials(where)
    .then(([rows]) => {
      res.send(rows);
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const read = (req, res) => {
  models.tutorial
    .findTutorial(req.params.id)
    .then(([rows]) => {
      if (rows[0] == null) {
        res.sendStatus(404);
      } else {
        res.send(rows[0]);
      }
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const edit = (req, res) => {
  const tutorial = req.body;

  // TODO validations (length, format...)

  tutorial.id = parseInt(req.params.id, 10);

  models.tutorial
    .update(tutorial)
    .then(([result]) => {
      if (result.affectedRows === 0) {
        res.sendStatus(404);
      } else {
        res.sendStatus(204);
      }
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const add = (req, res) => {
  const tutorial = req.body;

  // TODO validations (length, format...)

  models.tutorial
    .insert(tutorial)
    .then(([result]) => {
      res.location(`/tutorials/${result.insertId}`).sendStatus(201);
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const destroy = (req, res) => {
  models.tutorial
    .delete(req.params.id)
    .then(([result]) => {
      if (result.affectedRows === 0) {
        res.sendStatus(404);
      } else {
        res.sendStatus(204);
      }
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

module.exports = {
  browse,
  read,
  edit,
  add,
  destroy,
};
